package com.example.cinemacgp.model;

public class Movie {
    private Integer id, year;
    private Double score;
    private String synopsis, title, type, rating, image;

    public Movie(Integer id, Integer year, Double score, String synopsis, String title, String type, String rating, String image) {
        this.id = id;
        this.year = year;
        this.score = score;
        this.synopsis = synopsis;
        this.title = title;
        this.type = type;
        this.rating = rating;
        this.image = image;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Double getScore() {
        return score;
    }

    public void setScore(Double score) {
        this.score = score;
    }

    public String getSynopsis() {
        return synopsis;
    }

    public void setSynopsis(String synopsis) {
        this.synopsis = synopsis;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    @Override
    public String toString() {
        return title;
    }
}
